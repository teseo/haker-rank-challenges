    {
        $this->staircase = new Staircase();
    }

    public function testStaircaseCanBeResolved()
    {
        $this->assertTrue($this->staircase instanceof \Staircase\Staircase);
    }
    public function testStaircaseWithOneStep()
    {
        $steps = 1;

        $this->assertEquals('#', $this->staircase->printStaircase($steps));
    }
